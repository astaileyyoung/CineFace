CREATE TABLE headshots (
    cast_id             VARCHAR(16),
    headshot_id         CHAR(128),
    name                VARCHAR(255),
    x1                  FLOAT,
    y1                  FLOAT,
    x2                  FLOAT,
    y2                  FLOAT,
    img_height          INT,
    img_width           INT,
    pct_of_frame        FLOAT
);